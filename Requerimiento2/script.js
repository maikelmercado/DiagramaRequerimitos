
let num1 = parseInt(prompt("Ingresa un numero (mayor a 0)"));
let num2 = parseInt(prompt("Ingresa un numero (mayor a 0)"));

let suma = num1 + num2;
let resta = num1 - num2;
let division = num1 / num2;
let multiplicacion = num1 * num2;
let modulo = num1 % num2;

alert(`resultados: suma:${suma},resta:${resta},division:${division},multipliacion;${multiplicacion},modulo:${modulo}`);